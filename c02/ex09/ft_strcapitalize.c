char	*ft_strcapitalize(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[0] > 96 && str[0] < 123)
		{
			str[0] -= 32;
		}
		if (str[i] > 64 && str[i] < 91)
		{
			str[i] += 32;
		}
		if (str[i] > 96 && str[i] < 123 && str[i - 1] == 32 )
		{
			str[i] -= 32;
		}
		i++;
	}
	return (str);
}
